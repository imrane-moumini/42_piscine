#include <unistd.h>

void	ft_putnbr(int nb)
{
	long	n;
	char	num;

	n = nb;
	if (n < 0)
	{
		n = n * -1;
		write(1, "-", 1);
	}
	if (n <= 9)
	{
		num = n + 48;
		write(1, &num, 1);
	}
	else
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
}
